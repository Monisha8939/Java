//factorial program
import java.util.Scanner;
class factorial {
    public static void main(String args[]){
        Scanner s = new Scanner(System.in);
        int n=s.nextInt(); //get the input from the user
        int ans=1;

        for(int i=n;i>=1;i--){  // Loop to calculate the factorial by multiplying numbers from n to 1
           ans=ans*i;  // Multiply ans by i in each iteration
           
        }
        System.out.println(ans); //print the result
    }
}
